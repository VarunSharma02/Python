# to import time 
import time
timestamp = time.strftime('%H:%M:%S:%MS')
print(timestamp)
timestamp = time.strftime('%H')
print(timestamp)
timestamp = time.strftime('%M')
print(timestamp)
timestamp = time.strftime('%S')
print(timestamp)
timestamp = time.strftime('%MS')
print(timestamp)
